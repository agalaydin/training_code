# Сортировка списка

# Этот пример сортирует список. Сортировка - это часто используемаям задача,
# которую можно реальзовать множество строк кода с циклом, но можно ускорить
# свою работу при помощи встроенного метода сортировки.

my_list = ["leaf", "cherry", "fish"]
my_list1 = ["D", "C", "B", "A"]
my_list2 = [1, 2, 3, 4, 5]

my_list.sort()
my_list1.sort()
print(sorted(my_list2, reverse=True))
